/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  uint32_t temp;
  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */
  if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_RXNE) && 
      __HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_RXNE)) //收到一个字符中断
  {
    SysComm.RxBuf[SysComm.Index++] = (uint8_t)(huart1.Instance->DR & 0x00FF); // 读取数据寄存器
    // 防止缓冲区溢出
    if (SysComm.Index >= BufLen) SysComm.Index =0;
    __HAL_UART_CLEAR_FLAG(&huart1, UART_FLAG_RXNE); // 清除标志位
  }

  /* 处理空闲中断（IDLE）：一帧数据接收完成 */
  if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) && 
      __HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_IDLE)) //一帧数据接收结束
  {
    /* 清除空闲中断标志（需先读SR再读DR） */
    temp = huart1.Instance->SR;  // 读状态寄存器
    temp = huart1.Instance->DR;  // 读数据寄存器（清除标志）
    (void)temp; // 避免未使用变量警告
	  
    SysComm.EOR =1;
	 SysComm.RxLen =SysComm.Index;
	 SysComm.Index =0;
  }
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE END USART1_IRQn 1 */
}

